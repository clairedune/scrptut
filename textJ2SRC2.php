<H2>J2. Validation du cahier des charges</H2>


<div class="alert alert-info">

Ce second jalon est un jalon LIBRE (dans la limite de la plage de temps donnée dans le calendrier). 
C'est la validation du cahier des charges par le tuteur.
C'est donc une VALIDATION TUTEUR. 

</div>


<p>
Ce jalon est un jalon de validation du cahier des charges par le tuteur. Le début des réalisations ne peut commencer avant que le cahier des charges ne soit entièrement rédigé.
</p>

<p>
Ce jalon détermine la fin de la phase d'analyse et le début des réalisations.
</p>

<p>
Il n'est pas nécessaire d'attendre la correction du cahier des charges fonctionnel (courant décembre) pour procéder à cette validation. En revanche, toutes les modifications demandées dans cette correction seront à intégrer au cours de la réalisation, en tenant le tuteur informé des recommandations à mettre en oeuvre. 
</p>

<div class="alert alert-info">
Ce jalon donnera lieu au dépôt d'un document de validation. Une fois ce document déposé sur le gware, les réalisations peuvent commencer.</div>

