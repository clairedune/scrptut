<H2>L4. Support des réalisations</H2>
      					
<div class="alert alert-info">
Dans les dernières semaines de projets, vous recevrez une clé USB vous permettant de stocker toutes vos réalisations et vos 
livrables. Vous remettrez cette clé au responsabe de projet à l'issu de la soutenance.
</div>



<H3>1. Contenu</H3>
<p>
Lors de la soutenance, vous viendrez avec cette clef pour faire votre présentation sur un des ordinateurs disponibles. Vous devrez donc y copier tous les supports nécessaires à votre soutenance :</p>
<ul>
<li>le support que vous projeterez au format initial (keynote, Open Office, power point) + au format pdf au cas ou le document initial ne passe pas.</li>
<li>votre site internet</li>
<li>éventuellement, les films que vous diffuserez</li>
<li>éventuellement,les fichier audio</li>
<li>etc</li>
</ul>

<p>
En plus de vos supports de soutenance, vous devrez remettre toutes vos productions de l'année. Si vous avez assez de place sur la clé vous pouvez tout y mettre. S'il ne reste pas assez de place, ajouter des DVD gravés. 
</p>

<H3>2. Organisation des données </H3>

<ul>
<li>un répertoire LIVRABLES contenant tous vos livrables et vos validations de jalons en conservant la nomenclature du gware;</li>
<li>un répertoire SUIVI contenant tous vos compte rendus mensuel ET les fiches de suivi de votre tuteur;</li>
<li>un répertoire REALISATIONS contenant :</li>
<ul>
<li>un sous répertoire WEB contenant votre site web : sources, images et documents nécessaires </li>
<li>un sous répertoire COMMUNICATION contenant votre campagne de communication : affiche, publicité, cartes de visites etc</li>
<li>un sous répertoire VIDEO contenant les vidéos résultant de votre projet</li>
<li>un sous répertoire DIVERS contenant tous les documents que vous jugez utile pour qu'un groupe reprenne votre projet l'an prochain (doc technique sur votre projet, tutoriels sur des logiciels).</li>
</ul>
</ul>
</p>
<p>
Si vous n'avez pas réalisé de VIDEO dans votre projet, vous insérez quand même ce répertoire mais en le laissant vide.
</p>

<p>
Si vous n'avez pas assez de place sur la clé pour mettre toutes vos réalisations, vous fournirez en plus un support DVD.
</p>