<H3>3. Compte rendus de réunion</H3>

<a class="btn btn-small" href="files/compteRendu.doc">télécharger l'exemple de compte rendu de réunion </a>
	      					<br>
	<br>

<div class="alert alert-info">
Vous insérez ensuite dans le document tous vos comptes rendus de réunion du mois, les uns à la suite des autres avec la date bien apparente et 
les décisions prises lors de cette réunions nettement mise en évidence par l'usage de la couleur.
</div>

<p>
 Essayez, dés le début de l'année d'organiser des réunions formelles en convoquant tous les membres de l'agence, au minimum une fois par mois. 
 Elles jalonneront votre projet et vous permettront de formaliser les prises de décision afin de pouvoir vous y référer ultérieurement.
</p>

<p>
 Une réunion doit être préparée minutieusement avec un ordre du jour établi, un lieu et une heure précise. Soit le chef de projet gère les réunions et établi les ordres du jours, soit un autre membre de l'agence peut s'en charger.
</p>

<p>
 Au cours de la réunion, une personne gère le temps pour respecter l'ordre du jour et une autre personne prend des notes pour rédiger le 
 compte rendu. Vous noterez qui était présents, qui avaient prévenu de son absence et qui était absent sans justification.
</p>











