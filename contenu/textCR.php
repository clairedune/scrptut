	</div>	


<div class="alert alert-info">
Les compte rendu mensuels seront composés de trois éléments rassemblés dans le même document PDF :
<ul> 
<li>un rapport d'avancement,</li> 
<li>le suivi des heures et les comptes rendu de, </li>
<li>les comptes rendus de réunion du mois en cours;</li>
</ul>

<p>
Ils sont à poster tous les premiers du mois sur le gware.
</p>
</div>

<H3>1. Rapport d'avancement</H3>

<br>
<a class="btn btn-small" href="files/avancement.ppt">télécharger le rapport d'avancement</a>
<br>
	<br>


<div class="alert alert-info">
Le rapport d'avancement est un document synthétique d'une à deux pages maximum. Comme son nom l'indique il situe
l'avancement du projet. Il liste les problèmes et les points bloquants à faire remonter au pilote.
</div>

<H4> 1.1 Travail effectué durant le mois </H4>
<p>Ce document liste les tâches effectuées dans le mois courant en précisant pour chaque tâche :
<ul>
<li>Son nom ; </li>
<li>Un descriptif ;</li>
<li>Son statut (en cours / terminée / en retard) et éventuellement un pourcentage d'avancement</li>
<li>date de fin prévue ;</li>
<li>Le nom des personnes affectées à cette tâche;</li>
<li>Un commentaire expliquant si la tâche est en cours ce qu'il reste à faire, ce qui pose 
problème, des pistes de solution et les noms de ceux qui doivent terminer cette tâche.</li>
</ul> 
</p>

<p>Les tâches seront organisées, par exemple par catégories (informatique, infographie, marketing, etc.) ou bien par pourcentage de réalisation, 
en commençant par les tâches achevées</p>

<H4> 1.2 Planning du mois suivant </H4>

<p>
Le document se poursuit par la liste des tâches prévues pour le mois suivant avec pour chaque tâche le nom
des personnes à qui ces tâches sont attribuées. N'oubliez pas de reporter les tâche en cours dans cette répartition des tâches.
</p>

<H4> 1.3 Liste des points bloquants </H4>
<p>
Le document se termine par une liste de problèmes ou de points bloquant que l'équipe souhaite faire remonter à son tuteur
ou au pilote.
</p>

<H3>2. Suivi des heures </H3>

<p>
Pour vous donner une idée, pour faire 100 heures de projet sur le semestre, un étudiant doit travailler un peu plus de 20 par mois, donc un peu plus de 5h00 par semaine.
</p>

<div class="alert alert-info">
	<span class="label label-important">Important</span>
	Chaque agence devra dès le début du projet tenir au jour le jour 
	et durant toute l'année une comptabilité précise des heures passées sur le projet. 
	Elle devra recenser pour chaque membre de l'agence et pour chaque tâche :</p>
	<ul>
		<li>la date, le nombre d'heures passées 
		(il est nécessaire de descendre à un niveau très précis : éviter les blocs supérieurs à 4h) ;</li>
		<li>la désignation explicite de l'activité en quelques mots ;</li>
		<li>l'indication heures collectives (réunions) / heures individuelles.</li>
	</ul>
</div>

<p>
Le document comporte une partie par membre de l'agence. Pour chaque membre il détaille :
<ul>
	<li>les tâches effectuées dans le mois avec leur durée</li>
	<li>la somme des heures travaillées depuis le début du projet</li>
</ul>
</p>

<p>En plus des indications chiffrées, vous pouvez ajouter des le début de l'année un graphique de l'évolution des heures
de chacun pour que cela soit plus visuel.</p>


<p>
 Dans ce document, vous détaillerez également l'utilisation de ressources enseignantes ou étudiante.
</p>

