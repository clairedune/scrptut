<H2>J4. Validation des réalisations</H2>

<div class="alert alert-info">
Ce quatrième jalon est un jalon LIBRE (dans la limite de la plage de temps donnée dans le calendrier). 
C'est la validation de vos réalisations au regard de la commande de la fiche projet.
C'est une VALIDATION TUTEUR. Vous pouvez inviter d'autres enseignants en tant qu'expert sur un point particulier de vos réalisations. 
</div>

<p>
Lors de ce jalon, l'enseignant tuteur vérifie votre avancement par rapport à la fiche projet et donne les dernières recommandations 
par rapport à la soutenance.
</p>

<p>
Le format de ce jalon est libre et à déterminer avec votre tuteur. Vous pouvez présenter vos réalisations sous la forme que vous souhaitez (il serait une très bonne idée de s'en servir de présoutenance). L'objectif est de présenter toutes vos réalisations (incluant produit, campagne de communication et partie marketing).
</p>

<div class="alert alert-info">
Ce jalon donnera lieu au dépôt d'un document de validation. Une fois ce document déposé sur le gware, la phase de mise en production et valorisation peut commencer. Elle se terminera par le jalon suivant qui est la soutenance finale.</div>

