<dl>
	<dt>Enseignants</dt>
		<dd>Tous les enseignants du département sont ressources pour les projets tuteurés et peuvent être sollicités par les étudiants.</dd>
	<dt> Etudiants </dt>
		<dd>Les étudiants sont également ressources sur 
			base de volontariat. Pour être ressource, ils spécifient 
			les domaines de compétences sur lesquels ils souhaitent 
			intervenir ou citent éventuellement 
			des projets pour lesquels ils souhaitent être ressources. 
			Ils seront rémunérés en heures projet avec un maximum de 10 heures/an.
		</dd>
	</dl>
</dl>

<div class="alert alert-info"><!--debut alert utilisation des ressources-->
		Lorsqu'une agence utilise une ressource, que cela soit un étudiant ou un enseignant, 
		elle doit le mentionner dans son planning en précisant le nombre d'heures accordées par la ressource. <br>
		Lors du compte rendu de la réunion mensuelle, les agences préciseront quelle a été la contribution des
		personnes ressources durant le mois écoulé.			
</div> <!--fin alert utilisation des ressources-->
