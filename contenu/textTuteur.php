			
			<p>
				Chaque agence a un tuteur, qui est un enseignant du département SRC. 
				Il est désigné par le pilote de projet ou peut être démarché par les 
				étudiants lors de la conception de l'expression du besoin. Le tuteur 
				apporte son expérience dans la <b>réalisation du projet tuteuré</b>. 
				Il doit donc avoir les compétences techniques pour
				encadrer la part majoritaire du projet.
			</p>	
			<p>
				Etre tuteur d'une agence, <b>engage à assumer trois fonctions : suivre, relayer et évaluer.</b>
			<dl> 
				<dt>Suivi (1 fois par mois)</dt>
					<dd>Le tuteur assure le <b>suivi</b> (vérification des comptes rendus postés sur le gware) 
					et le <b>soutien</b> de l'agence (conseils). 
					Il ne doit en aucun cas faire le travail à la place des étudiants mais 
					peut leur fournir une assistance technique si nécessaire.
					Il oriente les étudiants <b>au moins une fois par mois</b> pour 
					définir et réaliser leur produit. Si le calendrier, de l'enseignant-tuteur ne lui
					permet pas de voir les étudiants durant un mois, il devra s'assurer qu'il n'y ait pas 
					plus de deux mois entre deux réunions.
				
							
					<div class="alert alert-info"><!--debut alert utilisation des ressources-->
						Lors de chaque réunion avec son agence, le tuteur doit remplir une fiche de suivi 
						et la déposer sur le gware : 
						<br>
						<br>
						<a class="btn btn-small" href="files/FicheSuivi.doc">
						télécharger la fiche de suivi </a><br>
						<br>
						<p>Ces fiches sont très synthétiques (1/2 page) et doivent contenir les remarques et 
						recommandations du tuteur 
						pour le suite du projet. Elles permettent aux étudiants de disposer d'une 
						trace écrite des décisions et aux pilotes de suivre l'avancement du projet.</p>		
						
						</div> <!--fin alert utilisation des ressources-->
					</dd>
				<dt>Relais du pilote</dt>
					<dd>Le tuteur assure un suivi de proximité de ses agences. 
						Il sert de relais au pilote dans le suivi au fil de 
						l'année de la réalisation du projet. 
						Si un problème survient (organisation, ressource humaine, 
						besoin en terme de matériel ou de financement) 
						il doit contacter le pilote de projet.
					</dd>
				<dt>Evaluation </dt>
					<dd><b>Note de suivi</b> : le tuteur n'évalue pas les livrables ni les soutenances du groupe qu'il encadre. 
					En revanche,  il attribue une note de suivi à la fin de l'année. Elle peut être identique 
					pour tous les étudiants du groupe ou individuelle.
					</dd>
					<dd><b>Corrections de livrables</b> : être tuteur engage à corriger un CDCF et un rapport de synthèse d'une  
					    autre agence que la sienne.
					</dd>
					<dd><b>Jury de soutenance</b> : être tuteur engage à participer aux jurys de soutenance.
					</dd>
			</dl>

