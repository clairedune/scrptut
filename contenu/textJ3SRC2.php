<H2>J3. Soutenance à mi-parcours</H2>
 				
		<div class ="alert alert-info">
			Le jalon 3 est une soutenance dont la date est fixée. C'est une VALIDATION PILOTE.
			Suite à la soutenance, une liste de recommandations vous sera remise. Vous devrez en tenir compte dans la suite de votre projet.
	</div>

 		
 				
 						<H3>1. Objectif </H3>
 						<p>
 							A mi parcours des projets, vous présenterez votre avancement.
 							L'objectif est de faire un point et de dialoguer avec le jury afin de modifier le plan de travail des
 							mois à venir si nécessaire.
 						</p>				
 						
 						<p>
 							La présence du tuteur et du client serait un plus lors de cette soutenance.
 						</p>
 						
 						<H3>2. Formalisme</H3>
 						
 						
 						<p>
 							Elle prendront soit la forme d'un marathon de soutenances, c'est à dire une série de petites soutenances portant chacune sur un domaine bien spécifique avec des jurys composés d'enseignants spécialistes du domaine. 
 						</p>
 						
 						<div class ="alert alert-info"> 
 							La forme de la présentation est libre. Dans ces soutenances, il sera laisser une grande part à la discussion.  
 						</div>
 						
 						<div class ="alert alert-info"> 
 							Chaque soutenance dure MAXIMUM 5' et est suivie de 15 à 20 minutes de question. Toutes les 30', un nouveau groupe entre dans la salle.  
 						</div>				
 						<p>
 							 							
							Vous pourrez vous appuyer sur des transparents conformes
							 à la charte graphique du projet avec éventuellement 
							 ajout de vidéos ou démonstration du produit sous sa forme actuelle.
 						</p>
 						
 						<p>
	 						La répartition du temps de parole est laissé à l'appréciation de l'agence. En revanche,
 							durant les questions, tous les membres de l'agence pourront être intérrogés.
 						</p>
 						
 						<p>
 						 Les étudiants présentent sur leur ordinateur personnel. Au besoin il peuvent demander un ordinateur au service technique du département au minimum 15 jours avant la date de la soutenance.
 						</p>
 						
 						<H3>3. Contenu </H3>

						<p>Les étudiants doivent se présenter avec toutes leurs réalisations ainsi qu'une page A4 présentant l'avancée de leurs travaux dans la matière.</p>
						<div class ="alert alert-info">
						Toute absence d'une document demandé par le jury peut mettre fin à la soutenance.
						</div>
						
					 	<H3>4. Evaluation </H3>
				
				
 	<p>
 		Un rapport est délivré par le jury à l'issu de la soutenance. Il contient une liste de recommandations qui 
 		devront être traitées avant la soutenance finale.
 		Ce rapport sera déposé sur le gware par le jury suite à la soutenance.
 	</p>

<p>
 En plus de chaque rapport, chaque groupe reçoit une note sur 5 points.
</p>

