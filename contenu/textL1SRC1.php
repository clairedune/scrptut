Les étudiants doivent constituer des agences de 4 à 6 personnes d'une même promotion. Une agence est non divisible. La séparation des membres de l'agence met fin au projet.
</p>

<p>
	Les étudiants peuvent au choix, reprendre un projet existant ou créer une nouvelle fiche projet.
</p>
 
 <p>
 	La composition de l'agence doit être en rapport
 	avec le projet visé. Vous devez penser à intégrer toutes les compétences nécessaires. 
 	Ne fonctionnez pas que par affinité, réfléchissez aux tâches à effectuer et aux compétences qu'elles requièrent 
 	</p>

<p>
 Le choix du tuteur est fait par le pilote des projets. Cependant, les étudiants peuvent proposer le nom d'un enseignant.
</p>
	
<p>
Les projets, doivent être en relation avec les thèmes de l'année et respecter les contraintes suivantes :

<ul>
	<li><b>Recouvrir l'ensemble des composantes de la formation</b></li>
	(par exemple, pas de projet uniquement audiovisuel ou dépourvu d'informatique...)
	<li><b>Etre économiquement réaliste</b> : tous les projets, qu’ils renvoient à des services 
	marchands ou non marchands ou qu’ils soient d’inspiration plus « artistique », doivent 
	s’appuyer sur la définition un modèle économique 	
	expliquant comment sont couverts au moins les coûts occasionnés par le fonctionnement. 
	Un projet non rentable peut évidemment être envisagé dès lors qu’il est réaliste.</li>
	<li><b>Présenter une utilité pour une ou plusieurs catégories d’usagers potentiels</b> : 
	le produit ou service multimédia est développé en 
	réponse à des besoins d’accès à l’information, de communication, de divertissement, de 
	promotion, de mise en relation, de formation, etc.... qui devront être étudiés.</li>
	<li>Être à la fois <b>innovant et créatif</b> d'une part ; <b>réaliste et réalisable</b> d'autre part, 
	compte tenu du temps disponible et du niveau général : il faudra trouver le compromis.</li>
</ul>
</p>