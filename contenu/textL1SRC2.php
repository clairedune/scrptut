<h2> L1. Documents de lancement de projet </h2>		
<p> 
		Les étudiants ont un mois pour constituer leurs équipes projets, 
		décrire l'expression du besoin dans une fiche projet, démarcher un 
 	tuteur et convenir avec lui de 2 réunions de jalons dans l'année pour valider le cahier des charges
 	focntionnel et valider les réalisations. 
 </p>
 <p>
 	Toutes ces informations seront consignées dans les trois documents 
 	qui constituent le premier livrable L1 : la convention de projet, 
 	la fiche projet et le phasage de l'année.	
 	Ils sont à déposer sur le gware.
</p>
	
<br>
<br>
	
<h3> L1.1 Convention</h3>

<br>
	<a class="btn btn-small" href="files/convention.doc">télécharger la convention</a>
<br>
<br>	
<p>Toutes les agences doivent télécharger la <b>convention</b> de projet, 
la remplir et la faire signer par le client, dans le cas ou il y a 
un client réél, et par le chef de département, dans le cas ou il n'y
a pas de client.</p>
	
<p>
La convention fixe la liste des étudiants qui constituent l'agence, le nom du client (IUT SRC/client réél) 
	le titre du projet et le nom du tuteur.
</p>	
	
<div class="alert alert-info">
	<span class="label label-important">Important</span> La convention ne couvre pas les aspects de propriété 
	intellectuelle. Un avenant spécifique doit être rédigé en cas de nécéssité.
	(voir au cas par cas avec le responsable des projets tuteurés.)
</div>


<br>
	
<h3> L1.2 Fiche projet</h3>
<br>
<a class="btn btn-small" href="files/fichetype.doc">télécharger la fiche projet</a>
<br>
<br>
<p>
	L'expression du besoin est consignée de manière synthétique dans une <b>fiche projet</b>. La fiche doit 
	être rédigée par le client, l'agence de projet et le tuteur. <b>Elle doit être validée par le responsable
	des projets avant que l'agence ne débute le projet</b>. En d'autres termes, tant que la fiche projet n'est pas validée, 
	le seul travail de l'agence est de travailler à une nouvelle version de cette fiche jusqu'à l'obtention de sa validation. 
</p>
	
<p>
Les thèmes de projet, doivent respecter les contraintes suivantes :

<ul>
	<li><b>Recouvrir l'ensemble des composantes de la formation</b></li>
	(par exemple, pas de projet uniquement audiovisuel ou dépourvu d'informatique...)
	<li><b>Etre économiquement réaliste</b> : tous les projets, qu’ils renvoient à des services 
	marchands ou non marchands ou qu’ils soient d’inspiration plus « artistique », doivent 
	s’appuyer sur la définition un modèle économique 	
	expliquant comment sont couverts au moins les coûts occasionnés par le fonctionnement. 
	Un projet non rentable peut évidemment être envisagé dès lors qu’il est réaliste.</li>
	<li><b>Présenter une utilité pour une ou plusieurs catégories d’usagers potentiels</b> : 
	le produit ou service multimédia est développé en 
	réponse à des besoins d’accès à l’information, de communication, de divertissement, de 
	promotion, de mise en relation, de formation, etc.... qui devront être étudiés.</li>
	<li>Être à la fois <b>innovant et créatif</b> d'une part ; <b>réaliste et réalisable</b> d'autre part, 
	compte tenu du temps disponible et du niveau général : il faudra trouver le compromis.</li>
</ul>
</p>
