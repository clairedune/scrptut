<h3> L1.3 Phasage</h3>

<br>
<a class="btn btn-small" href="files/SRC2phasage.doc">télécharger la fiche de phasage</a>
<br>
<br>

<p>
	Le phasage est simplement un document texte qui fixe la fin des phases du projet 
	en fixant <b>4 réunions de jalon</b>
	avec le tuteur et le pilote dés le début de l'année. Certaines phases sont déjà fixées. 
	D'autres ont des périodes libres.
 
	<ul>
		<li>Phase de définition - J1: validation fiche projet <b>pilote</b>; </li>
		<li>Phase d'analyse - J2 : validation spécification fonctionnelle <b>tuteur</b>;</li>
		<li><i>Phase de conception avancée (fixée)</i> - J3 : soutenance mi parcours <b>pilote</b>  ;</li>
		<li>Phase de réalisation - J4 : validation réalisations <b>tuteur</b> ;</li>
		<li><i>Phase de mise en production (fixée)</i>- J5 : soutenance finale <b>pilote</b>;</i></li>
	</ul>
</p>
<p>
	Lors de ces réunions, l'agence doit démontrer que la phase du projet en cours est achevée. 
</p>

<p>
   Le phasage fixe également le mode de fonctionnement entre le tuteur et l'agence : fréquence et jour des réunions, mode de 
   communication entre deux réunions (mail, gware, dropbox, etc.).
</p>

