<H2>L3. Site internet de présentation </H2>

<div class="alert alert-info">Tout au long de l'année, les étudiants de SRC1 devront développé un site
internet pour présenter le concept de leur projet</div>

<H3>1. Forme</H3>

<p>
	Ce site utilisera les compétences aquises en informatique (HTML/CSS, Javascript) et en infographie. En particulier,
	il devra intégrer la charte graphique du projet.
</p>


<H3>2. Fond</H3>

<p>
 Ce site web présente le concept du projet. S'il s'agit d'un projet vidéo, vous pourrez intégrer un épisode pilote ou encore un teaser du film qui sera réaliser l'année suivante.
</p>

<p>
	Il pourra présenter les différentes parties du cahiers des charges et pourra d'ailleurs servir de support de rendu pour le cahier des charges. Vous remmettrez alors au correcteur l'url du site. Les parties consacrées au cahier des charges pourront alors être protégées par un mot de passe.
</p>

