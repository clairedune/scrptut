			<h2> Le client</h2>

			<div class="alert alert-info">
			<span class="label label-important">Important</span>Pour tous les projets, que le client soit réel ou fictif, une convention de projets tuteurés devra être signée par l’agence, le département et le commanditaire pour formaliser cette relation.
			</div>

			<p>
				Sans que cela soit obligatoire, les agences peuvent entrer en relation avec une organisation ou une 
				institution externe qui jouera le rôle de commanditaire. Ce dernier pourra être par exemple une association 
				(à but artistique, culturel, sportif, social, humanitaire...) sans but lucratif mais en aucun cas une structure 
				qui pourrait payer la réalisation du projet (entreprise marchande, association de taille importante, collectivité locale...).
			</p>

			<p>
				La présence d'un commanditaire rend le projet plus concret. Elle offre la possibilité de gérer de manière effective 
				les relations liées à la prestation de services multimédia qui se trouvent au coeur de la gestion de projets. 
				C'est en particulier le cas de la définition des besoins, de l'analyse du contexte où le projet devra être implanté, 
				de l'intégration de ces contraintes et exigences dans la conception et dans la réalisation du projet.
				Ceci constitue à la fois une difficulté (changements d'avis potentiels, manque de disponibilité du commanditaire...) 
				et un avantage (plus grande facilité à dimensionner le projet, confrontation à la réalité, expérience professionnelle à part entière).
			</p>

			<p> 
				Quoi qu'il en soit, le commanditaire éventuel devra accepter l'organisation des projets tuteurés, en particulier les délais 
				et le fait qu'il s'agit d'une réalisation s'inscrivant dans le cadre d'un cursus universitaire : les agences ont obligation de moyens 
				(travailler régulièrement et tout mettre en oeuvre pour que le projet atteigne la meilleure qualité possible) mais en aucun cas une obligation de résultats (un produit parfaitement fonctionnel et totalement abouti). 
				Les agences ne seront pas au service du commanditaire et encore moins sous son autorité comme pourrait l’être un prestataire professionnel.
				
			</p>

			<p>
			Deux possibilités s'offrent aux étudiants :
			</p>
		
			<ul>
				<li>Proposer leur projet à un commanditaire au cours de la phase de définition : si les étudiants ont une idée de service multimédia, ils peuvent chercher quelqu'un à qui elle pourrait servir.</li>
				<li>Répondre à la demande d'un commanditaire extérieur : une association de leur connaissance a besoin d'un produit/service multimédia et les étudiants le conçoivent/le réalisent pour elle, 
				sous réserve que le thème respecte les contraintes fixées par le cadre pédagogique des projets tuteurés.</li>
