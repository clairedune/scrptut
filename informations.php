			<h1>Organisation de l'année <small> phasage, jalons, livrables </small></h1>
			<!-- Div de gauche ============================================-->
			<div class="row" >
			<div class="span6">
	 
			<h2>
				Phasage, jalon, livrables
			</h2>
	
			<p>	
				Les agences fonctionnent en mode projet en suivant une démarche par jalonnement. Le projet est découpé en phases :
			</p>  
			
			<ul>
				<li><strong>phase de définition</strong> : constitution des agences, définition du thème de projet, validation du thème et validation du livrable 1 ;</li>
				<li><strong>phase d'analyse </strong>: situer le projet dans son contexte, étudier l'existant, tracer une ligne éditoriale et décrire la définition du projet conformément au livrable 2. Vous devez ici cerner quelles sont les fonctions attendues, quel est le périmètre du projet, quels seront ses critères d'évaluation, ce qui est important et ce qui l'est moins. ;</li>
				<li><strong>phase de conception avancée </strong>: durant la phase de conception avancée, vous explorez et choisissez les solutions multimédias, graphiques, informatiques, réseaux, etc. afin de mettre en œuvre les fonctions de service (externes) identifiées dans le dossier de définition;</li>
				<li><strong>phase de réalisation</strong> : cette phase consistera en la mise en œuvre des spécifications techniques définies dans la phase de conception avancée.  ;</li>
				<li><strong>phase de mise en production </strong>: c'est le moment des enquetes de satisfaction et de la présentation du produit fini lors d'évènements et  de la soutenance. </li>
			</ul>

  


      <div class="alert alert-info">
			<span class="label label-important">Important</span> Chaque agence organise la plannification des phases de son projet. Elle comprend les dates de livraisons des prototypes et
			les dates de réunions de jalonnement avec le tuteur ou avec les pilotes. Cette organisation est décrite dans le <b>document de phasage</b>, 
			validé par le tuteur. </div>

			<p> 
				Chaque phase se termine par un <b>jalon</b> (ou <it>revues de projet</it>) qui permettent de faire le point sur le projet.
				La phase suivante n'est engagée que si la phase en cours est validée</b>. 
			<p>
			</p>
				Lors de la revue de projet terminant une phase, <b>Le tuteur et/ou le pilote</b> examine les livrables et décide de la validation de la phase. 
				Les décisions actées lors des revues de changement de phase sont des éléments stables sur lesquels peut se bâtir la suite du projet. 
				Le jalonnement se focalise sur l'appréciation des livrables par le client, le tuteur et le pilote.
			</p>
			
			<div class="alert alert-info">
			<span class="label label-important">Important</span> Chaque jalon donne lui à la rédaction d'une note de validation par le tuteur, le client ou le pilote.
			On y évalue la progression du travail en formulant des remarques sur la phase écoulée et suggestions pour la suite du projet. Cette note de synthèse doit être livrée sur l'espace de travail du gware et envoyé par mail au pilote du projet.
			</div>
			

			
