				<p><span class="label label-important">Important</span>
			 
					Chaque agence se forme à l'intérieur de sa promotion, c'est à dire, <b>chaque promotion est étanche</b> 
					pour des raisons de compétences et de calendrier.
					<b>Les agences sont indéfectibles</b> durant toute l’année (toute séparation des membres de l'agence met fin au projet).
				</p>
			</div>

			<p> 
				Les agences disposent d'une large autonomie du point de vue de leur organisation. 
				Elles s'organisent comme des agences de production multimédia : chaque agence aura une politique de management propre, 
				des fonctions précises devront être affectées à chaque membre. Voici quelques fonctions essentielles aux projets :
			</p>
			<dl>
				<dt>Chef de projet</dt> 
					<dd>Chargé de la coordination générale du projet, de la planification des tâches et de la livraison des livrables.</dd>
				<dt> <b>Responsable de communication</b> 
					<dd>chargé de la gestion des documents du projet (collecte, archivage...) et de la rédaction des compte rendus de réunion.</dd>
				<dt>Responsable logistique</dt>
					<dd>chargé de la planification et de la gestion du matériel (audiovisuel, informatique…).</dd>
				<dt>Responsables de composantes</dt> 
					<dd>ce sont les designers, webmasters, développeurs, commerciaux etc…; </dd>
			</dl>
		</div><!-- /Div de gauche ==-->

			<div class="span6">
			<p> 
				Vu le nombre restreint d'étudiants par agence et le nombre important de fonctions, 
				une même personne sera amenée à assurer plusieurs fonctions. Les fonctions pourront évoluer dans l'année.
				Le fait d'être responsable d'une composante ne signifie pas que l'étudiant concerné devra 
				assurer l'intégralité du travail correspondant, mais qu'il devra en coordonner la réalisation.  
			</p>

			<p> 
				Chaque étudiant devra intervenir un peu à tous les niveaux, même si l'on peut évidemment admettre qu'il y 
