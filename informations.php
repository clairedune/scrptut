<!-- Reporting================================================== -->			
		<div class="tab-pane" id="reporting">
			<h1>Reporting <small> décompte des heures et suivis </small></h1>
			
			<!-- Div de gauche ============================================-->
			<div class="row" style="text-align:justify;">
			<div class="span6">
			<p> 
					Le programme pédagogique national (PPN) du DUT SRC indique que les étudiants doivent effectuer individuellement 
					300 heures au titre des projets tuteurés sur les deux années de la formation.
			</p>
			
			<p> Ce temps intègre à la fois les activités de conception, de réalisation, d'auto-formation éventuelle aux 
					différentes techniques ainsi que les temps des réunions et de rédaction des livrables.
			</p>


			<div class="alert alert-info">
			<p><span class="label label-important">Important</span>
			Chaque agence devra dès le début du projet tenir au jour le jour 
			et durant toute l'année une comptabilité précise des heures passées sur le projet. 
			Elle devra recenser pour chaque membre de l'agence et pour chaque tâche :</p>
			<ul>
				<li>la date, le nombre d'heures passées (il est nécessaire de descendre à un niveau très précis : éviter les blocs supérieurs à 4h) ;</li>
				<li>la désignation explicite de l'activité en quelques mots ;</li>
				<li>l'indication heures collectives (réunions) / heures individuelles.</li>
			</ul>
		</div>


		<div class="well">Lorsque tous les membres de l'agence sont présents à une réunion d'une heure, 
			chaque membre ajoute une heure à ses heures effectuées pour le projet. 
			Si vous êtes 6 membres, une réunion d'une heure coûte 6h00 au projet.
		</div>
		</div><!-- /Div de gauche ============================================-->
	
			<div class="span6">
		
		<div class="alert alert-info"> 
		<p><span class="label label-important">Important</span> La note de chaque étudiant sera ramenée au pro-rata du nombre d'heures effectuées. 
		Le surplus d’heures n’apporte aucune bonification de note.
		</p>
		<p>
		ex: Un étudiant devait faire 200 heures de projet dans l'année. Il n'en a fait que 120 et son agence a obtenu 14. Il recevra donc 14 * 120 / 200 = 8.4.
		</p>
		</div>
		
		<p>Cette démarche poursuit plusieurs objectifs :</p>
		<ul>
			<li>Apprendre à estimer le temps nécessité par chaque tâche, ce qui aide à élaborer la planification et à l'ajuster (suivi du réalisé); </li>
			<li>S'immerger dans une logique professionnelle où le temps passé sur un projet doit être évalué avec une grande précision pour estimer les coûts de revient</li>
			<li>Vérifier que chaque membre de l'agence apporte une contribution à peu près équivalente aux autres et respecte globalement les heures attendues : en cas de décalage trop important, la note finale sera pondérée pour chaque étudiant à hauteur du temps passé / de l’investissement sur le projet.</li>
		</ul>

		<p>
			Les déclarations reposent sur la confiance. Chaque mois, une vérification des fiches d'heures sera effectuée par les pilotes. 
			En cas de déclarations manifestement surévaluées, pas assez détaillées ou insuffisamment justifiées, des réajustements pourront 
			être effectués par le tuteur ou le pilote. Un suivi régulier sera opéré au cours de l'année par le Tuteur.
		</p>

		<p> 
			En plus des revues de projets décrites dans la suite, les agences doivent déposer une compte rendu d'avancement une fois par mois sur le gware. 
			Il se présente sous la forme d'un document d'une page précisant les activités du mois et la répartition des tâches pour les activités du mois suivant.
		</p>


			
