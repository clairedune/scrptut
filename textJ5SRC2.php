<H2>J5. Soutenance</H2>
      					
<p>
	Vous devrez présenter une réalisation qui consistera dans l’idéal 
	en un produit fini opérationnel, ou au moins en un prototype le 				
	plus abouti possible. Le degré de finalisation sera pris en compte 
	dans l’évaluation, mais sera mis en relation avec la difficulté générale 
	de votre projet, les qualités de méthode et d’organisation dont vous aurez 
	fait preuve, les justifications que vous apporterez. Vous devrez démontrer 
	que vous avez rempli au mieux les objectifs fixés par votre fiche projet.
</p>
<p>
	Le jury d'évaluation sera composé d'enseignants de tous les domaines de la 
	formation SRC. Vous devrez donc démontrer en quoi vos réalisations couvrent tous ces domaines.
</p>


<H3>1. Formalisme</H3>

<div class ="alert alert-info"> 
	La soutenance sera divisée en deux parties : 20 minutes de présentation dont 5 minutes
	de présentation en anglais et 10 minutes de questions/réponses.
</div>

<p> Vous présenterez en utilisant des supports informatiques (slide, site web, etc) et des vidéos, 
suivant le matériel disponible.</p>

<div class ="alert alert-info">
 La présence de tous les membres est obligatoire. Tous les membres de l'agence doivent 
 prendre la parole en anglais et en français.
 L'absence d'un étudiant à la soutenance entraine la note 0/20 pour son année de projet.
</div>


<p>
 La soutenance doit être scénarisée et la scénarisation doit être en adéquation avec votre projet
 (voir cours de communication). La scénarisation inclu le choix de la tenue qui devra être
       professionnelle (soit costume/tailleur, soit 
	identité visuelle cohérente pour le groupe). Soignez votre attitude, evitez les tenues 
	négligées, le maintien douteux comme mains dans les poches ou 
	fesses sur un bord de table, surtout dans les moments ou vous ne parlez pas.
</p>
	
    


<H3>2. Contenu</H3>
      					
<p>
	Vous devez présenter le produit réalisé ainsi que la campagne marketing que vous avez mené pour le faire	
	connaître de votre cible.
</p>

<p>
	Vous devez convaincre l'ensemble du jury du volume horaire effectué. (la répartition des heures sur l'année pourra 	
	vous être demandée, soyez préparés).
</p>

<p>
	Le jury évaluera à la fois la <b>part technique</b> du projet et la <b>dimension force de vente</b> 
	de votre présentation. 
	A vous de trouver le juste équilibre. <b>Tout n'est pas bon à dire, réfléchissez !</b> Rien de 
	vos discordes ne doit apparaitre, sauf si c'est pour en tirer des enseignements formateurs. Même ligne 
	de conduite pour les problèmes de tout ordre: concluez par du positif ou taisez-vous pour ne pas 
	compromettre votre réussite.
</p>

<div class ="alert alert-info">
Quelques conseils:
<ul>
	<li>
	Commencez par présenter le produit pour finir par 
	le montage du projet pour ne pas être surpris par le temps qui passe très vite ;
	</li>
	<li>
	N'oubliez pas de rappeler la commande qui vous a été faite et la réponse que vous proposez ;
	</li>
	<li>
	Toutes sortes de documents que vous jugerez utiles peuvent être proposés au jury en début de présentation ;
	</li>
	<li>
	Montrez toutes vos réalisations de l'année : campagne d'affichage, spot publicitaires, newletters, cartes
	de visites, obtention de sponsoring, etc... 
	</li>
</ul>
</div>

