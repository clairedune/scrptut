<H2>J3. Validation technique à mi-parcours</H2>
 				
		<div class ="alert alert-info">
			Le jalon 3 est une soutenance dont la date est fixée. C'est une VALIDATION PILOTE.
			Suite à la soutenance, un rapport sera déposé sur le gware listant des recommandations
			à prendre en compte dans la suite du projet.
	</div>

 		
 				
 						<H3>1. Objectif </H3>
 						<p>
 							A mi parcours des projets, vous présenterez votre avancement sous la forme d'une soutenance technique.
 							L'objectif est de faire un point et de dialoguer avec le jury afin de modifier le plan de travail des
 							mois à venir si nécessaire.
 						</p>				
 						
 						<p>
 							La présence du tuteur et du client serait un plus lors de cette soutenance.
 						</p>
 						
 						<H3>2. Formalisme</H3>
 						
 						<div class ="alert alert-info"> 
 							La soutenance sera divisée en deux parties : 10 minutes de présentation et 15 minutes de questions/réponses.
 						</div>
 						
 						 						
 						<p>
 							La forme de la présentation est assez libre.
 							
							Vous devez vous appuyer sur des transparents conformes
							 à la charte graphique du projet avec éventuellement 
							 ajout de vidéos ou démonstration du produit sous sa forme actuelle.
 						</p>
 						
 						<p>
	 						La répartition du temps de parole est laissé à l'appréciation de l'agence. En revanche,
 							durant les questions, tous les membres de l'agence pourront être intérrogés.
 						</p>
 						
 						<H3>3. Contenu </H3>

						<ul>
						<li><H4>Fiche projet</H4> 
						amener une version imprimée de votre fiche projet pour les membres du jury.</li>
						
						<li> <H4>Etat d'avancement du projet</H4> 
					       <ul>
					       <li><b>informatique</b> : présentation de la base de données et du développement web minimum démandé 
					       pour chaque projet</li>
					       <li><b>web design & video</b> : présentation du design graphique, prouver que le template est 
					       <i>maison</i>, validation 
					       de la partie ergonomie par envoie de l'URL 1 semaine avant la soutenance. 
					       & <b>si vidéo</b> : état d’avancement (recrutement des acteurs, avancement du scénario, outillage)</li>
						 	<li><b>marketing & communication </b> : analyse de marché & concurrence, stratégie marketing, 
						 	plan de communication, ligne éditoriale</li>
						 	</ul>
						 </li>
						 <li> <H4>Questions / réponses</H4> 
						 <ul>
						 <li>
						   Gestion de projet ;
						 </li> 
						 <li>
						 	Retour sur des points techniques (prévoyez vos codes et vos réalisations) ;
						 </li>
						 <li>
						 	Etablissement d'une liste de modifications à apporter au projet, si nécessaire. Toutes les modifications
						 	demandées devront être appliquées et visible lors de la soutenance finale.
						 </li>
						 </ul>
						 </ul>

							 
					 <H3>4. Notation </H3>
				
 	<p>
 		Un rapport est délivré par le jury à l'issu de la soutenance. Il contient une liste de recommandations qui 
 		devront être traitées avant la soutenance finale.
 		Ce rapport sera déposé sur le gware par le jury suite à la soutenance.
 	</p>


