<H2>J1. Validation de la fiche projet</H2>


<div class="alert alert-info">

Ce premier Jalon est LIBRE (dans les limites de la plage de temps fixée par le calendrier). C'est la validation de la fiche projet par les responsables des projets.
C'est donc une VALIDATION PILOTE. 

</div>


<p>
Tant que la fiche projet n'est pas validée, le seul travail de l'équipe et de la modifier jusqu'à ce qu'un accord soit trouvé avec les responsables de projet.
Si la fiche est validée avant le mois d'octobre, le Jalon 1 est considéré comme validé.
</p>

<div class="alert alert-info">
Ce jalon donnera lieu au dépôt d'un document de validation, accompagné de la fiche projet définitive sur le gware. C'est ce document qui vaut pour engagement.
</div>

