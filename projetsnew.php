  <H3>			
  	Service communication pour les enseignants de SRC
  </H3>
  <p>Le titre de ce projet est un peu provocateur. Il suit une idée qui me trotte dans la tête depuis un moment : pourquoi ne pas proposer
  aux enseignants de jolies trames pour faire leur site web perso, leurs supports de cours, leurs transparents, leurs cartes de visite. </p>		

		 <hr class="soften">
		 
		 <H3>			
		 SRC inversé
		   </H3>
  <p>L'idée est de proposer une plateforme web permettant aux enseignants de diffuser leur cours au format vidéo. Il existe beaucoup de ces plateformes dans les écoles américaines mais encore aucune en France.</p>		



			
<H3>
	Participer à la création du journal en ligne de l'école primaire de Fréjus  (A affiner)
</H3>
<p>Je n'ai pas encore pris contact avec les enseignants de cette école primaire, je vous livre donc une premiere interpretation du
projet proposé par Yann qui sera modifiée des le contact pris.</p>

<p>
	Les enseignants de l'école primaire de Frejus proposent des ateliers web aux élèves de leurs classes.
	L'idée serait d'aider ces élèves à mettre en ligne leur gazette en leur proposant des solutions techniques
	mais aussi en formant les enfants. Ce projet a donc une très grande dimension pédagogique : 
	comment adapter les outils web actuels à la diffusion de contenus rédigés / crées par des enfants. 
	Les contenus peuvent également être des contenus vidéos ou encore des images.	
</p>
		
		 <hr class="soften">

       
  <H3>			
  Trier à l'université 
  </H3>
  <p>Proposer une solution de trie des déchets au département et à l'université</p>

		<p>Ce projet consiste à monter un dossier complet qui sera proposé à l'université à la fin de l'année. L'objectif est de proposer en amont une solution de tri réaliste et à la portée de tous et dans tous les bâtiments de l'université. Le dossier devra contenir une solution clé en main, communication inclue. Le département SRC pourra cette année servir de laboratoire à petite échelle pour tester la solution proposée.</p>
		
		<p>Il faut donc prospécter auprès des entreprises de collecte de déchets dans la région, créer et distribuer les contenaires et sensibiliser les étudiants, les enseignants et les personnels techniques et admninistratif au tri des déchets.</p>
	
		
 <hr class="soften">

      
      <H3>
      		Restaurant "Le Cristal" a Frejus 
      </H3>

	  <p>Campagne de communication pour le restaurant "le Cristal" à Frejus</p>
        
        <p>Le restaurant ‘Le Cristal’ est implanté à Fréjus, 1513 avenue De Lattre de Tassigny, et fait partie de l’ESAT (établissement et service d’aide par le travail) les Mimosas. Ce restaurant intègre et forme des travailleurs handicapés pour les faire accéder à une vie professionnelle.</p>

<p>Le restaurant « Le cristal » vous accueille pour (petit) déjeuner sur place, propose également les formules à emporter. Il existe aussi la possibilité de louer des salles entièrement équipées en vue d’organiser des séminaires.</p>
         
         <p>
         Son directeur Bruno Rollet souhaite faire davantage connaître localement son 
établissement, augmenter le nombre de repas servis, de formules à emporter et 
communiquer sur les salles disponibles à louer pour l’organisation de séminaires.
Il s’agit donc de mesurer le potentiel de cet établissement, d’estimer le chiffre d’affaires 
correspondant et pour cela, réfléchir à sa communication off et on line.
         </p>
      
<hr class="soften">
      
      
      <H3>
       Gestion interne du suivi des stages et des projets 
      </H3>
        <p>Concevoir un outil web permettant de gérer le suivi des stages et des projets.</p>
        
        <p>
        	Dans le cadre des projets tuteurés et des stages, les étudiants et les enseignants doivent remplir des documents administratifs de suivi. Cette application vise à centraliser tous les documents et les fiches de notation sur une même plateforme.
        </p>    
        
        <p>Une réflexion doit être mené sur l'ergonomie du site et un soin tout particulier sera porté à sécurité. Les enseignants et les étudiants doivent être formés à l'utilisation de ce site.</p>


<hr class="soften">

    
<H3>    Courts métrages </H3>
        <p>Création d'une agence de réalisation audio-visuelle spécialisée dans la réalisation de formats courts. </p>
        
        <p>Le groupe de projet doit réaliser de bout en bout un ou plusieurs court métrages dans l’année : 
           rédaction de scénario, recherche de lieux de tournage, organisation de castings, prise de son et 
           création originale des musiques, tournage, montage, etc... Il devra également en assurer la diffusion 
           et la promotion auprès des étudiants du département SRC/MMI, (voir auprès d’un public plus large). 
           Le (ou les) film(s) produits seront soumis au festival SRC.
           Un intérêt tout particulier sera portée sur les aspects juridiques liés au projet (droit de tournage,
           droit à l'image, droits de diffusion).      
           </p>
           <p>
           En seconde année, les étudiants devront également développer une application web. Ils développeront une
           plateforme permettant à un public à définir de dépôser des courts métrages qui seront évalués par les internautes.
           </p>
  
  
<hr class="soften">
      
      <H3> Web série </H3>
      
        <p>Création et diffusion en ligne d'une web série. </p>
        <p>
         	Réalisation d'au moins 3 épisodes de 10 minutes qui seront diffusés en ligne. Le groupe de projet
         	devra assurer la réalisation des épisode de bout en bout ainsi que leur promotion et leur diffusion.
         	Un intérêt tout particulier sera portée sur les aspects juridiques liés au projet (droit de tournage,
           droit à l'image, droits de diffusion). L'impact de la web série devra être mesurée par la mise en place
           d'un indicateur d'audience et de campagne marketing.      
         </p>
         <p>	
         	En seconde année, le groupe projet devra également développer une application web permettant la mise en 
         	ligne pereine de tutoriels videos.
        </p>

