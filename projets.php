  <div class="accordion-group">
    <div class="accordion-heading">
      <a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion2" href="#collapseTri">
			Trier à l'université
      </a>
    </div>
    <div id="collapseTri" class="accordion-body collapse">
      <div class="accordion-inner">
        <p>Proposer une solution de trie des déchets au département et à l'université</p>

		<p>Ce projet consiste à monter un dossier complet qui sera proposé à l'université à la fin de l'année. L'objectif est de convaincre la présidence de 
		mettre en place une solution de tri à la portée de tous et dans tous les bâtiments de l'université. Le dossier devra contenir une solution clé en main, communication inclue.Le département SRC pourra cette année servir de laboratoire à petite échelle pour tester la solution proposée.</p>
		
		<p>Il faut donc prospécter auprès des entreprises de collecte de déchets dans la région, créer et distribuer les contenaires et sensibiliser les étudiants, les enseignants et les personnels techniques et admninistratif au tri des déchets.</p>
	
		<p>En deuxième année, les étudiants doivent concevoir une application web orchestrant la récolte des déchets sur les différents sites de l'université.</p>
 
      </div>
    </div>
  </div>


    <div class="accordion-group">
    <div class="accordion-heading">
      <a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion2" href="#collapseMoon">
       Spectacle "Moon can dance"
      </a>
    </div>
    <div id="collapseMoon" class="accordion-body collapse">
      <div class="accordion-inner">
        <p>Production d'un spectacle chorégraphique d'une heure environ avec 3 danseurs professionnels sur le thème de". la légende de la Lune"</p>

		<p>Ce projet couvre plusieurs volets :</p>
			<ul>
				<li> composition (par des musiciens dont Michel Raynaud) édition et mixage de la musique avec Protools</li>
				<li> filmer et monter une vidéo promotionnelle</li>
				<li> concevoir un site internet qui présentera le spectacle</li>
			</ul>

		<p>Ce projet demande du traitement du son et de la vidéo. D'autre part, la pub et les maquettes demanderont un traitement de l'image. C'est un projet complet dans le cadre de notre enseignement qu'on pourra modeler à notre guise pour l'adapter à chaque étudiant du groupe de projet.</p>

      </div>
    </div>
  </div>
  <div class="accordion-group">
    <div class="accordion-heading">
      <a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion2" href="#collapseSix">
       Performance artistique air DJ (kinect)
      </a>
    </div>
    <div id="collapseSix" class="accordion-body collapse">
      <div class="accordion-inner">
        <p>Realisation d'une performance artistique basée sur la captation de mouvements de danse en utilisant la kinect</p>

		<p>Les enseignants du département SRC souhaitent organisée une exposition autour des arts multimédia. Cette exposition comprendra un espace photo, un espace 
		graphisme 2D et un espace vidéo.</p>
		
		<p>Dans l'espace vidéo, nous souhaiterions créer un performance artistique basée sur la captation des mouvement du public. Les mouvements des spectateurs feront s'animer en rythmes des formes projetées. En plus de projections de formes, les attitutes et les mouvements du public pourront être utilisée pour modifier en rythme des musiques</p>

		<p>Les étudiants devront donc avoir de bonnes connaissances en programmation et être prêt à apprendre un nouveau langage. Il devront également proposer des séquences graphiques à animer, voire composer la musique ou les paroles qui seront diffusées. Il faut également mettre en place cette performance artistique de bout en bout et en assurer la communication. C'est donc un projet très riche qui demande toutes les compétences acquises dans la formation SRC.</p>
      </div>
    </div>
  </div>

  
  <div class="accordion-group">
    <div class="accordion-heading">
      <a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion2" href="#collapseSeven">
       Restaurant "Le Cristal" a Frejus
      </a>
    </div>
<div id="collapseSeven" class="accordion-body collapse">
      <div class="accordion-inner">
        <p>Campagne de communication pour le restaurant "le Cristal" à Frejus</p>
        
        <p>Le restaurant ‘Le Cristal’ est implanté à Fréjus, 1513 avenue De Lattre de Tassigny, et fait partie de l’ESAT (établissement et service d’aide par le travail) les Mimosas. Ce restaurant intègre et forme des travailleurs handicapés pour les faire accéder à une vie professionnelle.</p>

<p>Le restaurant « Le cristal » vous accueille pour (petit) déjeuner sur place, propose également les formules à emporter. Il existe aussi la possibilité de louer des salles entièrement équipées en vue d’organiser des séminaires.</p>
         
         <p>
         Son directeur Bruno Rollet souhaite faire davantage connaître localement son 
établissement, augmenter le nombre de repas servis, de formules à emporter et 
communiquer sur les salles disponibles à louer pour l’organisation de séminaires.
Il s’agit donc de mesurer le potentiel de cet établissement, d’estimer le chiffre d’affaires 
correspondant et pour cela, réfléchir à sa communication off et on line.
         </p>
      </div>
    </div>
  </div>

  <div class="accordion-group">
    <div class="accordion-heading">
      <a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion2" href="#collapseOne">
        Courts métrages 
      </a>
    </div>
    <div id="collapseOne" class="accordion-body collapse">
      <div class="accordion-inner">
        <p>Création d'une agence de réalisation audio-visuelle spécialisée dans la réalisation de formats courts. </p>
        
        <p>Le groupe de projet doit réaliser de bout en bout un ou plusieurs court métrages dans l’année : 
           rédaction de scénario, recherche de lieux de tournage, organisation de castings, prise de son et 
           création originale des musiques, tournage, montage, etc... Il devra également en assurer la diffusion 
           et la promotion auprès des étudiants du département SRC/MMI, (voir auprès d’un public plus large). 
           Le (ou les) film(s) produits seront soumis au festival SRC.
           Un intérêt tout particulier sera portée sur les aspects juridiques liés au projet (droit de tournage,
           droit à l'image, droits de diffusion).      
           </p>
           <p>
           En seconde année, les étudiants devront également développer une application web. Ils développeront une
           plateforme permettant à un public à définir de dépôser des courts métrages qui seront évalués par les internautes.
           </p>
      </div>
    </div>
  </div>
    <div class="accordion-group">
      <div class="accordion-heading">
      <a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion2" href="#collapseClip">
        Clips vidéos
      </a>
    </div>
    <div id="collapseClip" class="accordion-body collapse">
      <div class="accordion-inner">
        <p>Promouvoir les jeunes artistes en leur proposant d'enregistrer leur premier clip</p>
        
        <p>
         Aller à la rencontre d'artistes ou de groupes de musique locaux pour leur proposer de tourner leur premier clip. 
         Cette agence réalise des clips vidéo en tout genres (films, animations 3D, stop motion, etc…) et les diffuse sur
         sa plateforme en ligne.
           </p>
           <p>
           En seconde année, les étudiants devront également développer une application web. Ils pourront par exemple 
           créer un formulaire en ligne pour que les groupes déposent leur maquette et s'inscrivent. Ensuite, ils pourront
           suivre l'évolution de leur clip en temps réél en se connectant sur le site.
           </p>
      </div>     
    </div>
  </div>

  
