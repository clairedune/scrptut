       Gazette du département
      </a>
    </div>
    <div id="collapseFive" class="accordion-body collapse">
      <div class="accordion-inner">
        <p>Edition d'un mensuel institutionnel sur l'actualité du département SRC</p>
        <p>
           Du papier ! Une fois n'est pas coutume, ce projet est principalement un projet imprimé. L'agence de projet se transforme
           en véritable bureau d'édition avec son rédacteur en chef, ses journalistes, ses pigistes, ses graphistes. L'objectif est de diffiser un
           opus par mois entre les mois d'octobre et avril.
        </p>
        
        <p>
        	En deuxième année, un site web accompagnera la gazette papier. On y retrouvera tous les articles du format papier avec des
        	bonus et la possibilité de commenter les publications. Aux étudiants de trouver une solutions pour synchroniser les textes de
        	la version papier et de la version en ligne. La mise en page sera sans doute très différente dans ce format web, à rélfléchir...
        </p>
         
        
      </div>
    </div>
