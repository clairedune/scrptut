<H2>L4. Dossier de synthèse</H2>
      					
<p>C'est le rapport final du projet tuteuré. Vous devrez présenter une réalisation qui consistera dans 
l’idéal en un produit fini opérationnel, ou au moins en un prototype le plus abouti possible. 
Le degré de finalisation sera pris en compte dans l’évaluation, mais sera mis en relation avec 
la difficulté générale de votre projet, les qualités de méthode et d’organisation dont vous aurez fait preuve, 
les justifications que vous apporterez.
</p>


<p>
Ce rapport complète la soutenance en donnant un bilan comptable de vos réalisations. 
Il doit prouver que vous avez rempli tous le 
objectif de la fiche projet qui vous a été confiée en début d'année.
En particulier, vous devez rendre en annexe un cahier de test exhaustif 
de vos produits multimédia. 
</p>

<H3>1. Contenu</H3>

<p>Vous trouverez ci dessous un example de plan de rapport avec le détail du contenu des parties. Le nombre de page est donné à titre indicatif. Vous
pouvez adopter ce plan ou le modifier, cependant vous devez remplir toutes ces parties.</p>

<div class="well">
<H4>Page de titre<H4>	
<H4>Bordereau qualité</H4>
<H4>Introduction</H4>
<H4>Sommaire</H4>
<H4>I. Le produit final</H4>
<H5>I.1 Synthèse des réalisations (3 pages)</H5>
<p>Présentation du produit final du projet. C'est une <b>présentation synthétique du coeur de votre projet</b>. </p>
<p>Plusieurs cas de figure selon que votre projet concerne un ou plusieurs de ces points: </p>
<ul>
<li>
Si le projet consistait à organiser un évènement, présentez cet évènement de manière factuelle : à quelle date a t'il eu lieu ? Dans quel lieu ? Combien de participants ? Parmi ces participants, combien d'étudiants de l'iut ? Quel a été le déroulement de l'évènement ? </li>
<li>Si le projet consistait à réaliser une vidéo, présentez le synopsis de cette vidéo, la longueur de la vidéo. Indiquez à quelles occasions elle a été diffusée. Indiquez, si elle est en ligne, l'adresse où la visionner. Faire quelques insertions d'images.</li> 
<li>Si le projet consiste à réaliser un outil web : vous présenterez ses interfaces et ses fonctionalités selon la catégorie d'usager.</li>
<li>Si le projet consiste à la création de produits dérivés : vous présenterez les produits réalisés.</li>
<li>Etc…</li>
</ul>

<H5>I.2. Synthèse du cahier des tests (2 pages)</H5>

<p>Vous présentez ici la synthèse des tests effectués. Vous détaillerez tous ces tests en annexe. Vous pouvez présenter les informations sous forme de graphes ou de camemberts représentant par exemple :</p>
<ul>
<li>le nombre de tests passés, </li>
<li>le nombres de tests positifs/négatif.</li> 
<li>le nombre de fonctionnalités existantes à la fin du projet par rapport au nombre de fonctionnalités prévues dans le cahier des charges,</li>
<li>etc…</li>
</ul>

<p>C'est donc une représentation statistique qui est attendue. Selon les projets, la liste des tests peut être plus ou moins longue. Par exemple, un projet vidéo aura très peu de tests : on vérifiera juste que la vidéo respecte les spécifications du concours de SRC et est visible en ligne. A l'inverse, pour un site web, la liste des fonctionalités est beaucoup plus longue et peut faire plus de 4 pages très rapidement. Dans cette partie, vous ne ferez pas apparaitre le detail de tous ces tests mais une synthèse. La liste EXHAUSTIVE des tests vérifiant les exigences du CDCF, avec leurs résultats est OBLIGATOIRE EN ANNEXE.</p>

<H4>II . Bilan de la campagne de communication (3 pages)</H4> 

<H5>II.1. Organisation</H5>
<p>Comment avez vous organisé votre campagne de communication autour de votre produit pour le faire connaître ? Quelle image avez vous choisi de diffuser ? Vous vous appuyerez sur les supports de communication (actions, affiches, site, etc..) en précisant les lieux et périodes de visibilité.</p>

<H5>II.2. Résultats obtenus</H5>
<p>Quelle a été l'audience ? Est-ce que les étudiants ont entendu parler du projet ? Quelle a été la participation aux évenements ? Est-ce que les enseignants ont entendu parler du projet ? </p>
<p>Quelle est la fréquentation du site internet, et sa progression, d'où viennent les visiteurs ? </p>

<H4>III. Bilan marketing</H4>
<H5>III.1. Partenariats (1 page)</H5>
<p>Vous présenterez ici vos partenariats et les contrats qui vous lient.</p>
<H5>III.2 Enquête de satisfaction (2 pages)</H5>
<p>
Ici, vous présentez un retour des utilisateurs et du client sur le produit final livré. Vous pourrez par exemple vous appuyer sur une enquête de type sphynx.
</p>
<H5>III.3. Analyse SWOT (2 pages)</H5>
<p>Vous présenterez ici une analyse SWOT de votre projet.</p>  


<H4>IV. Gestion de projet</H4>
<H5>IV.1 Plannification sur l'année (2 pages)</H5>
<p>Comparaison des diagrammes de Gantt prévisionnel et réel du projet et répartition des heures entre les différents membres de l'agence.</p>
<p>Commentaire en cas d'arrêt de production.</p>

<H5>IV.2 Bilan financier (1 page)</H5>
<p>Ici pas de longs discours, juste un tableau de synthèse.</p>

<p>Vous donnerez ici votre bilan financier réel en précisant quelles ont été vos dépenses et vos entrées d'argent. Vous ferez également apparaître les acquisitions de biens en nature (lots, prêt d'une salle de cinéma, matériel de l'iut, etc).</p>

<p>Vous mentionnerez le coût de revient des prestations et une estimation de la marge pour obtenir une estimation du prix de vente de la prestation (incluant, site web, affiche, flyers, etc.), achat de logiciel et matériel informatique  (compter 1/5 de la valeur d'achat en amortissement)</p>


<H4>V. Perspectives de ré-utilisation</H4>
<H5>V.1. Mise en place de moyens de pérénisation (1/2 page)</H5> 
<p>Qu'avez vous mis en place pour permettre à un groupe futur de reprendre votre projet : tutoriel, documentation des codes, livraison d'une archive du site et d'un backup de la base de données, etc.</p>

<H5>V.2. Poursuite du projet (1/2 page)</H5>
<p>Y a t'il des fonctionnalités que vous n'avez pas eues le temps d'aborder ? Si un groupe devait continuer sur cette fiche projet l'année prochaine quel projet proposer ? Réécrivez la fiche projet telle qu'elle pourrait être proposée l'an prochain.</p>

<H5>V.3. Perpectives (1/2 page)</H5>
<p> Ce projet ouvre peut être la possibilité à d'autres projets qui pourrait s'appuyer sur vos réalisation. Quels seraient ces projets ? Vous pouvez rédiger cette partie sous forme de fiches.</p>

<H4>VI. Expression libre par rapport aux projets tuteurés (non évalué)</H4>
<p>Message pour les étudiants des années à venir ou pour les encadrants de projet.</p>

<H4>Conclusion<H4>
