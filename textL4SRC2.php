<H3>2. Forme</H3>
   
   <div class ="alert alert-info"><span class="label label-important">Important</span> 
            	    <b>Qualité du français</b> : 
            	    tout document comportant un trop grand nombre de fautes de français sera considéré 
            	    comme <i>illisible</i> et ne sera pas évalué. Il devra être corrigé par les étudiants
            	    et soumis à nouveau avec un malus de 4 points.</div>
   
   
   <p>
   	Par ailleurs, vous devrez respecter les règles suivantes :
            	
            	<ul>
               		<li><b>Format</b>
            		<ul>
            			<li>Dossier au format pdf (word, openoffice, indesign)</li>
            			<li>Taille maximale : 6Mo </li>
            		</ul>
            		</li>
            		<li><b>Mise en page</b>
            		<ul>
            			<li>Respect de la charte graphique du projet;</li>
            			<li>Présence d'une page de titre (nom du projet, liste des étudiants, nom du tuteur, classe, logo SRC et logo USTV, date du rendu et nom du rendu)</li>
            			<li>Présence d'un sommaire ;</li>
            			<li>Présence d'une introduction et d'une conclusion ;</li>
            			<li>Lisibilité des titres ;</li>
            			<li>Numérotation des pages ;</li>
            			<li>Mise en valeur des mots ou phrases clefs par l'utilisation du gras ou de la couleur ;</li>
            			<li>Texte justifié ;</li>
            			<li>Police de taille équivalente à Times new roman 10-12pt.</li>
            		</ul>
            		</li>
            		<li><b>Borderau qualité</b> 
            			<ul>	
            			<li> Un tableau de diffusion du document servant à lister toutes les personnes
				qui ont reçues ce document (professeurs, membres du groupe, validateur) contenant 4 colonnes :
				<ul>
					<li>nom de la personne</li>
					<li>la date à laquelle le document a été transmis</li>
					<li>la version du document transmis</li>
					<li>les  observations/ commentaires</li>
				</ul>
			</li>
			<li> Un tableau des versions du document :
				<ul>
					<li>numéro de la version (1.0 la première fois puis selon le type 
					de modification 1.01 (petites modifications) - 
					1.1 modifications de structure +2.0 modifications radicales)</li>
					<li>date de la création de la version</li>
					<li>types de modification de la version</li>
				</ul>
			</li>	
		</li>
		</ul>					
            		<li><b>Annexes obligatoires</b></li>
            		<ul>
            			<li>Bibliographie et webographie (liste des sources concurrentes, liste des sources d'inspiration, lien vers les médiathèques,...)</li>	
						<li>Cahier de tests détaillé</li>
						<li>Bilan des heures (valant pour notation)</li>
						<li>Comptes rendus mensuels</li>
						<li>Fiche projet</li>	            		
            		</ul>
            	</ul>
   </p>
   
